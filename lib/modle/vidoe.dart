import 'package:flutter/cupertino.dart';

class video {
  final String title;
  final String imagePath;
  final String userImage;
  final String subTitle;
  final String time;
  final Icon icon;

  const video({
    required this.title,
    required this.imagePath,
    required this.userImage,
    required this.subTitle,
    required this.time,
    required this.icon,
  });
}
